function displayAge(name, age) {

   return `${name} is ${age} yeas old.`;

}

function displayCity(name, city) {

   return `${name} lives in ${city}.`;

}

function displayHobby(name, hobby) {

   return `${name} has ${hobby} as their hobby.`;

}

export {

   displayAge,
   displayCity,
   displayHobby

}